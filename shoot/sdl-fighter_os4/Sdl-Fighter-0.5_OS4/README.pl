*******************
*   SDL-Fighter   *
*******************
Version: 0.5

Uzywane biblioteki to:
- SDL
- SDL_mixer
- SDL_image

Aby skompilowac SDL-Fighter wprowadz:
        make
Nastepnie jako root:
        make install
aby poprawnie zainstalowac wszystkie pliki gry.

Po instalacji mozesz uruchomic gre wprowadzajac:
        SDL-Fighter

