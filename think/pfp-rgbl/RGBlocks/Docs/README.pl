******************
*    RGBlocks    *
******************
Version: 2.0

RGBlocks to zaawansowana gra w stylu tetris z wieloma specjalnymi efektami (obracajace pole gry,
strzelanie, itd), eksplozjami, konsola, efektami dzwiekowymi i muzyka. Zagraj aby poznac reszte
cech.

Uzywane biblioteki to:
- SDL
- SDL_mixer
- GL, GLU

Aby skompilowac RGBlocks wprowadz:
        make
Nastepnie jako root:
        make install
aby poprawnie zainstalowac wszystkie pliki gry.

Po instalacji mozesz uruchomic gre wprowadzajac:
        RGBlocks
