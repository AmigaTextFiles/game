/************************************************************************/
/*																		*/
/*								You have died							*/
/*																		*/
/************************************************************************/

void DEADCode(PLAYER *p)
{
	char *s = p->Name;
	if (*s == '\0') s = "the peasant";
	DRAWText("\nWe mourn the passing of %s and his computer.\n\n",s);
	DRAWText("Dost thou wish resurrection ?\n\n");

	if (DEADGetYesNo())
	{
		DRAWText("Yes.\n");
		MAINSetup();
	}
}

/************************************************************************/
/*																		*/
/*							Get a yes or no								*/
/*																		*/
/************************************************************************/

int DEADGetYesNo(void)
{
	char c;
	do
		c = HWGetKey();
	while (c != 'Y' && c != 'N');
	return (c == 'Y');
}

